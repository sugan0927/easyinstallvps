class TypeScriptWorkerPlugin(BasePlugin):

    def get_metadata(self) -> PluginMetadata:
        return PluginMetadata(
            name        = "ts-worker",
            version     = "1.0.0",
            description = "TypeScript Cloudflare Worker project scaffolding with Wrangler, Vitest, and type defs",
            author      = "EasyInstall",
            requires    = ["node", "npm"],
            provides    = ["ts-worker", "worker-types", "vitest-tests"],
        )

    def initialize(self) -> bool:
        if not self.require_binary("node"):
            self.logger.warning("Node.js not found — needed for TypeScript compilation")
        if not self.require_binary("npm"):
            self.logger.warning("npm not found")
        return True

    @staticmethod
    def _safe(domain: str) -> str:
        return domain.replace(".", "-").replace("_", "-")

    def scaffold(self, domain: str, output_dir: str = "/tmp") -> str:
        """
        Scaffold a complete TypeScript Worker project for the given domain.
        Returns the project root directory path.
        """
        safe    = self._safe(domain)
        project = Path(output_dir) / f"{safe}-worker"
        src     = project / "src"
        src.mkdir(parents=True, exist_ok=True)

        # Source
        (src / "index.ts").write_text(TS_INDEX_TEMPLATE.format(domain=domain))
        (src / "index.test.ts").write_text(TS_TEST_TEMPLATE)

        # Config files
        (project / "tsconfig.json").write_text(TSCONFIG_TEMPLATE)
        (project / "package.json").write_text(
            PACKAGE_JSON_TEMPLATE.format(safe=safe)
        )
        (project / "wrangler.toml").write_text(
            WRANGLER_TOML_TS_TEMPLATE.format(domain=domain, safe=safe)
        )
        (project / "deno.json").write_text(
            DENO_IMPORT_MAP_TEMPLATE
        )
        (project / ".gitignore").write_text(
            "node_modules/\ndist/\n.wrangler/\n*.env\n"
        )

        self.logger.info(f"TypeScript worker project scaffolded: {project}")
        return str(project)

    def install_deps(self, project_dir: str) -> bool:
        """Run npm install in the project directory."""
        if not self.require_binary("npm"):
            return False
        result = subprocess.run(
            ["npm", "install", "--silent"],
            cwd=project_dir, capture_output=True, text=True
        )
        if result.returncode == 0:
            self.logger.info(f"npm deps installed in {project_dir}")
            return True
        self.logger.error(result.stderr)
        return False

    def build(self, project_dir: str) -> bool:
        """Compile TypeScript via tsc."""
        if not self.require_binary("tsc"):
            # Try via npx
            result = subprocess.run(
                ["npx", "tsc", "--noEmit"],
                cwd=project_dir, capture_output=True, text=True
            )
        else:
            result = subprocess.run(
                ["tsc", "--noEmit"],
                cwd=project_dir, capture_output=True, text=True
            )
        if result.returncode == 0:
            self.logger.info("TypeScript compilation successful")
            return True
        self.logger.error(f"TypeScript errors:\n{result.stdout}\n{result.stderr}")
        return False

    def run_tests(self, project_dir: str) -> bool:
        """Run Vitest tests."""
        result = subprocess.run(
            ["npx", "vitest", "run"],
            cwd=project_dir, capture_output=True, text=True
        )
        if result.returncode == 0:
            self.logger.info("All tests passed")
            return True
        self.logger.error(f"Tests failed:\n{result.stdout}")
        return False

    def deploy(self, project_dir: str, env: str = "production") -> bool:
        """Deploy the TypeScript worker via wrangler."""
        if not self.require_binary("wrangler"):
            result = subprocess.run(
                ["npx", "wrangler", "deploy", "--env", env],
                cwd=project_dir, capture_output=True, text=True
            )
        else:
            result = subprocess.run(
                ["wrangler", "deploy", "--env", env],
                cwd=project_dir, capture_output=True, text=True
            )
        if result.returncode == 0:
            self.logger.info(f"Worker deployed ({env})")
            return True
        self.logger.error(result.stderr)
        return False

    def generate_type_defs(self, output_path: str = "/tmp/worker-types.d.ts") -> str:
        """Write a minimal standalone type definition file for offline use."""
        dts = """\
// EasyInstall Worker — minimal type definitions (offline)
// For full types: npm install @cloudflare/workers-types

interface KVNamespace {
  get(key: string): Promise<string | null>;
  put(key: string, value: string, options?: { expirationTtl?: number }): Promise<void>;
  delete(key: string): Promise<void>;
}

interface ExecutionContext {
  waitUntil(promise: Promise<unknown>): void;
  passThroughOnException(): void;
}

interface ExportedHandler<Env = {}> {
  fetch(request: Request, env: Env, ctx: ExecutionContext): Response | Promise<Response>;
}

declare function caches(): { default: Cache };
"""
        Path(output_path).write_text(dts)
        self.logger.info(f"Type definitions written: {output_path}")
        return output_path
