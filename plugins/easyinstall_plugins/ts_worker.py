TS_INDEX_TEMPLATE = """\
/**
 * EasyInstall WordPress Edge Worker — TypeScript
 * Domain: {domain}
 * Generated by easyinstall-plugin ts-worker
 */

export interface Env {{
  RATE_LIMIT_KV?: KVNamespace;
  WS_SECRET?:     string;
  ENVIRONMENT?:   string;
}}

const BYPASS_PATHS: string[] = [
  '/wp-admin/', '/wp-login.php', '/wp-json/', '/?wc-ajax=',
];

const STATIC_PATTERN = /\\.(css|js|png|jpg|jpeg|gif|svg|ico|woff2?)(\\?.*)?$/i;

async function handleRequest(
  request: Request,
  env:     Env,
  ctx:     ExecutionContext,
): Promise<Response> {{
  const url      = new URL(request.url);
  const shouldBypass =
    request.method !== 'GET' ||
    BYPASS_PATHS.some(p => url.pathname.startsWith(p)) ||
    (request.headers.get('Cookie') ?? '').includes('wordpress_logged_in');

  if (shouldBypass) {{
    return addSecurityHeaders(await fetch(request));
  }}

  const cache    = caches.default;
  const cacheKey = new Request(url.toString());

  const cached = await cache.match(cacheKey);
  if (cached) return cached;

  const response = await fetch(request);
  if (response.status === 200) {{
    const ttl = STATIC_PATTERN.test(url.pathname) ? 86400 : 300;
    const clone = new Response(response.clone().body, response);
    clone.headers.set('Cache-Control', `public, max-age=${{ttl}}`);
    ctx.waitUntil(cache.put(cacheKey, clone));
  }}
  return addSecurityHeaders(response);
}}

function addSecurityHeaders(response: Response): Response {{
  const r = new Response(response.body, response);
  r.headers.set('X-Frame-Options',          'SAMEORIGIN');
  r.headers.set('X-Content-Type-Options',    'nosniff');
  r.headers.set('Strict-Transport-Security', 'max-age=31536000; includeSubDomains');
  r.headers.set('Referrer-Policy',           'strict-origin-when-cross-origin');
  r.headers.delete('X-Powered-By');
  return r;
}}

export default {{
  fetch: handleRequest,
}} satisfies ExportedHandler<Env>;
"""

TS_TEST_TEMPLATE = """\
/**
 * Vitest tests for EasyInstall WordPress Edge Worker
 * Run: npx vitest run
 */
import {{ describe, it, expect, vi, beforeEach }} from 'vitest';
import worker from './index';

const makeRequest = (url: string, opts: RequestInit = {{}}) =>
  new Request(url, {{ method: 'GET', ...opts }});

const mockEnv   = {{}};
const mockCtx   = {{ waitUntil: vi.fn(), passThroughOnException: vi.fn() }};

// Mock global fetch
global.fetch = vi.fn().mockResolvedValue(
  new Response('<html>WordPress</html>', {{
    status:  200,
    headers: {{ 'Content-Type': 'text/html' }},
  }})
);

describe('EasyInstall Worker', () => {{
  beforeEach(() => vi.clearAllMocks());

  it('returns a response for normal requests', async () => {{
    const resp = await worker.fetch(makeRequest('https://example.com/'), mockEnv, mockCtx);
    expect(resp.status).toBe(200);
  }});

  it('adds security headers', async () => {{
    const resp = await worker.fetch(makeRequest('https://example.com/'), mockEnv, mockCtx);
    expect(resp.headers.get('X-Frame-Options')).toBe('SAMEORIGIN');
    expect(resp.headers.get('X-Content-Type-Options')).toBe('nosniff');
  }});

  it('bypasses cache for wp-admin paths', async () => {{
    const resp = await worker.fetch(
      makeRequest('https://example.com/wp-admin/'), mockEnv, mockCtx
    );
    expect(resp.status).toBe(200);
    // waitUntil should NOT be called (no cache put)
    expect(mockCtx.waitUntil).not.toHaveBeenCalled();
  }});

  it('bypasses cache for logged-in users', async () => {{
    const resp = await worker.fetch(
      makeRequest('https://example.com/', {{
        headers: {{ Cookie: 'wordpress_logged_in_abc=1' }},
      }}),
      mockEnv, mockCtx
    );
    expect(resp.status).toBe(200);
  }});
}});
"""

